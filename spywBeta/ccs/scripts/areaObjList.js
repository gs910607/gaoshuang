areaObjList = areaObjList.filter(function(value){
    return !!value.areaCode && !!value.areaId && !!value.mcu;
});

function areaObj(options) {
	this.options = $.extend({
						districtId: null, //区县ID
						countyId: null,  //乡镇ID
					},options);

	this.areaObjList = areaObjList;

	var _this = this;

	this.init();

	$(this.options.districtId).on("change", function() {
		$(_this.options.countyId).html('<option value="">请选择</option>')
		var thisCode = $(this).val();
		_this.areaObjList.map(function(o,i){
			if(o.areaCode.length == 6 && o.areaCode.substr(0,3) == thisCode && o.areaId) {
				$(_this.options.countyId).append('<option value="'+ o.areaCode +'">'+ o.areaName +'</option>')
			}
		})
	})
}

areaObj.prototype.init = function() {
	this.drawDist();
}

areaObj.prototype.drawDist = function() {
	var _this = this;
	$(_this.options.districtId).html('<option value="">请选择</option>');
	_this.areaObjList.map(function(o,i){
		if(o.areaCode.toString().length == 3 && o.areaId) {
			$(_this.options.districtId).append('<option value="'+ o.areaCode +'">'+ o.areaName +'</option>');
		}
	})
}
